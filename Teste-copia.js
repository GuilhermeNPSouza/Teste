var b1 = document.querySelector('#b1');
var d42 = document.querySelector('#d42');

b1.addEventListener('click', function(){

  if(d42.style.display === 'block'){
    d42.style.display = 'none';
  }
  else{
    d42.style.display = 'block';
  }
});


